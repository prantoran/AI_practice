#this evaluation function uses the built in countScore() function
#parameters player1Score and player2Score scores to calculate
#the cutoff value

def evalFunction(gameState):
    prevTurn=gameState.currentTurn
    gameState.currentTurn=curP
    
    gameState.countScore()
    val = 0
    if curP==1:
        val=(gameState.player1Score-gameState.player2Score)
    else:
        val=(gameState.player2Score-gameState.player1Score)

    gameState.currentTurn=prevTurn
    return val
    
#Alpha of parent Max function is passed as parentAlpha argument

def minValue(gameState,depth,parentAlpha):
    
    beta = inf
    curAction = 0
    
    if gameState.pieceCount==42 or not depth:
        evalValue=evalFunction(gameState)
        return point(evalValue,0)
    
    depth=int(depth)
    newDepth=int(depth)-1

    nextTurn=1
    curTurn=gameState.currentTurn
    
    if gameState.currentTurn==1:
        nextTurn=2

    for i in range(7):
        result=gameState.playPiece(i)
        if result:
            gameState.currentTurn=nextTurn
            newV=0
            newAction=3
            newV,newAction=maxValue(gameState,newDepth,beta)
         
            if newV < beta:
                beta=newV
                curAction=i
            gameState.gameBoard[result-1][i]=0
            gameState.pieceCount-=1
        gameState.currentTurn=curTurn

        if beta <= parentAlpha:           #pruning
            return point(beta,curAction)
        
    return point(beta,curAction)
    

#Beta of parent Min function is passed as parentBeta argument

def maxValue(gameState,depth,parentBeta):
    alpha=-inf
    curAction=0
    
    depth=int(depth)

    if gameState.pieceCount==42 or not depth:
        evalValue=evalFunction(gameState)
        return point(evalValue,0)
    
    newDepth=int(depth)-1
    
    nextTurn=1
    curTurn=gameState.currentTurn
    
